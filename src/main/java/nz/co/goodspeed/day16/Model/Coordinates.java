package nz.co.goodspeed.day16.Model;

public class Coordinates {
    int x,y;

    public Coordinates(int x, int y) {
        this.x = x;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
